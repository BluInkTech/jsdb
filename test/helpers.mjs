export const getTempDir = () => {
	const tempDir = path.join(os.tmpdir(), Math.random().toString(36).substring(7))
	mkdirSync(tempDir, { recursive: true })
	return tempDir
}

export const deleteTempDir = (dirPath) => {
	rmSync(dirPath, { recursive: true, force: true })
}
